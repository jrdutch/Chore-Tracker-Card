// Resolve a translation: exact language, base language ("de-AT" → "de"),
// then English. {placeholders} are filled from vars.
export function makeLocalizer(lang) {
  const base = String(lang || 'en').toLowerCase();
  const dict = TRANSLATIONS[base] || TRANSLATIONS[base.split('-')[0]] || TRANSLATIONS.en;
  return (key, vars) => {
    let str = dict[key] ?? TRANSLATIONS.en[key] ?? key;
    if (Array.isArray(str)) return str;
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(`{${k}}`, v);
      }
    }
    return str;
  };
}
